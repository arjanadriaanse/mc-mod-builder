package twintro.minecraft.modbuilder.data.resources.meta;

/**
 * Contains information on the resourcepack for minecraft.
 */
public class PackResource {
	/**
	 * An integer describing the pack format. Usually this is 1.
	 */
	public int pack_format;
	/**
	 * A string describing the content of the resource pack.
	 */
	public String description;
}
